thu = input("Nhập thứ (1-7): ")
n_thu = int(thu)
if n_thu == 1:
    print("Sunday")
elif n_thu == 2:
    print("Monday")
elif n_thu == 3:
    print("Tuesday")
elif n_thu == 4:
    print("Wednesday")
elif n_thu == 5:
    print("Thursday")
elif n_thu == 6:
    print("Friday")
elif n_thu == 7:
    print("Saturday")
else:
    print("Không hợp lệ")