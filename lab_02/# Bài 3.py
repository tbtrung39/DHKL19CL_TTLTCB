thu = input("Nhập thứ (1-7): ")
n_thu = int(thu)
if n_thu == 1:
    print("Thứ 2")
elif n_thu == 2:
    print("Thứ 3")
elif n_thu == 3:
    print("Thứ 4")
elif n_thu == 4:
    print("Thứ 5")
elif n_thu == 5:
    print("Thứ 6")
elif n_thu == 6:
    print("Thứ 7")
elif n_thu == 7:
    print("Chủ nhật")
else:
    print("Nhập sai thứ, vui lòng nhập lại từ 1 đến 7")
    